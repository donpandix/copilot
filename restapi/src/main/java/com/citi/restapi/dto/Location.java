package com.citi.restapi.dto;

public class Location {
    private String city;
    private String state;

    // getters and setters
    public String getCity() {
        return city;
    }
    public void setCity(String city) {
        this.city = city;
    }
    public String getState() {
        return state;
    }
    public void setState(String state) {
        this.state = state;
    }





}